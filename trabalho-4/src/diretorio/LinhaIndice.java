package diretorio;

public class LinhaIndice {
    public String codigo;
    public String nomeArquivo;
    public String usuario;
    public String grupo;

    public LinhaIndice(String codigo, String nomeArquivo, String usuario, String grupo) {
        this.codigo = codigo;
        this.nomeArquivo = nomeArquivo;
        this.usuario = usuario;
        this.grupo = grupo;
    }
}
